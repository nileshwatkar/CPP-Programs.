//using switch case statement.

#include<iostream>
using namespace std;

int main()
{
    int day=7;

        switch(day)
            {

            case 1:
                    cout<<"\n Monday";
                    break;

            case 2:
                    cout<<"\n Tuseday";
                    break;

            case 3:
                    cout<<"\n wednesday";
                    break;

            case 4:
                    cout<<"\n Thursday";
                    break;

            case 5:
                    cout<<"\n Friday";
                    break;
            case 6:
                    cout<<"\n Satusrday";
                    break;

            case 7:
                    cout<<"\n Sunday";
                    break;

            default:
                    cout<<"\n The Day is Not Available";

            }
return 0;

}
//Output: Thursday
